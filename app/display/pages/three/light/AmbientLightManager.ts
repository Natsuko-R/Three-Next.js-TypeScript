//-----------------------------------------------------------------------------//
// インポート || import
//-----------------------------------------------------------------------------//
import { AmbientLight, Scene } from "three"

//-----------------------------------------------------------------------------//
// 定数 || constant
//-----------------------------------------------------------------------------//
const LIGHT_COLOR: number = 0xffffff
const LIGHT_STRENGTH: number = 2.5

//-----------------------------------------------------------------------------//
//  環境光源を管理するクラス
//  クラス名　　：  AmbientLightManager
//  引数　　　　：  Scene
//-----------------------------------------------------------------------------//
export default class AmbientLightManager {
  //-----------------------------------------------------------------------------//
  // フィールド || field
  //-----------------------------------------------------------------------------//
  private ambientLight: AmbientLight

  //-----------------------------------------------------------------------------//
  // コンストラクタ || constructor
  //-----------------------------------------------------------------------------//
  public constructor(scene: Scene) {
    // フィールドにインスタンスを作成する
    this.ambientLight = new AmbientLight(LIGHT_COLOR, LIGHT_STRENGTH)

    // シーンに追加する
    scene.add(this.ambientLight)
  }

  //-----------------------------------------------------------------------------//
  // メソッド || method
  //-----------------------------------------------------------------------------//

  // 終了処理
  public terminate() {
    this.ambientLight.dispose()
  }
}
